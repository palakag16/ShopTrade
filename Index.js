function filterData(tag) {
  const q = tag.innerHTML;
  let mappedUsers;
  if(q!=="All Products"){
    const filterdataByTag = data.filter((item) => item.tag == q);
   mappedUsers = filterdataByTag.map(prodsnipp).join("");
  }
  else{
   mappedUsers = data.map(prodsnipp).join("");
  }
  prodwrapper.innerHTML = mappedUsers;
  const parent = tag.parentElement.childNodes;
  for(let j of parent){
    j.style.border="1px solid #c4c4c4"
  }
  tag.style.border="1px solid #000"
}

